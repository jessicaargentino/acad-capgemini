package modulo01.aula02;

public class TipoLong {
    public static void main(String[] args) {
        long menor_valor = Long.MIN_VALUE; //-9223372036854775808 (64 bits)
        long maior_valor = Long.MAX_VALUE; //9223372036854775807 (64 bits)

        System.out.println(menor_valor);
        System.out.println(maior_valor);

    }
}
