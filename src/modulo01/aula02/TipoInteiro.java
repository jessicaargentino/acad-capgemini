package modulo01.aula02;

public class TipoInteiro {
    public static void main(String[] args) {

        int numero1 = 'A'; //65
        int numero2 = 10;
        int numero3 = -10;
        int menor_valor = Integer.MIN_VALUE; //-2147483648 (32 bits)
        int maior_valor = Integer.MAX_VALUE; //2147483647 (32 bits)

        System.out.println(numero1);
        System.out.println(numero2);
        System.out.println(numero3);
        System.out.println(menor_valor);
        System.out.println(maior_valor);
    }
}
